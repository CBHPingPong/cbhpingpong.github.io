		<h5>Áp dụng từ ngày 26/10/2020</h5>
<p>
	Căn cứ vào tình hình thực tế của CLB và nhu cầu học tập bóng bàn của các bạn học sinh trường THPT Chuyên Biên Hoà. Căn cứ vào ngày sinh hoạt đầu của CLB, BCN đã tổ chức họp các KeyMember và xây dựng phương hướng hoạt động và quy chế tổ chức CLB cho thời gian tới.
Về phương hướng hoạt động trong thời gian tới, CLB chú trọng việc phát huy tính chủ động của các thành viên, tạo điều kiện tốt cho các thành viên có thể thể hiện khả năng chơi bóng bàn của mình. Tích cực xây dựng các chương trình, hoạt động ngoại khóa tạo sự năng động cho các bạn học sinh trong trường. Giao lưu, liên kết và hợp tác với các CLB khác, tham gia các buổi văn nghệ do Đoàn trường và Nhà trường tổ chức.  Tham gia và hỗ trợ tích cực các hoạt động do Đoàn trường và Nhà trường tổ chức và đóng góp xây dựng phong trào chung của THPT Chuyên Biên Hoà.
Về quy chế tổ chức của CLB, BCN đã thống nhất với các Keymember và đưa ra quy chế và hình thức hoạt động cho thời gian tới như sau:<br>
 
Điều 1. Tên gọi, mục đích, chức năng của Câu lạc bộ<br>
• Tên gọi: CPC – CBH Ping Pong Club<br>
 
Mục đích của Câu lạc bộ:<br>
• Xây dựng môi trường để học sinh trường THPT Chuyên Biên Hoà, những người chơi bóng bàn và yêu thích bóng bàn có thể chơi bóng bàn và thực hành các kĩ năng bóng bàn.<br>
• Tổ chức bồi dưỡng kiến thức, kỹ năng về khả năng chơi bóng bàn thông qua các buổi tập luyện của Câu lạc bộ<br>
• Tạo nơi giao lưu, học hỏi, trao đổi kinh nghiệm học bóng bàn cho những người yêu thích bóng bàn, muốn rèn luyện các kỹ năng bóng bàn.<br>
• Xây dựng một địa chỉ tư vấn phương pháp học và rèn luyện bóng bàn cho HS trong trường, và là nơi HS vui chơi, giải trí sau mỗi buổi học căng thẳng<br>
• Tạo dựng một sân chơi lành mạnh, sôi nổi, và bổ ích, thu hút đông đảo HS tham gia, góp phần làm phong phú đời sống sinh hoạt tập thể cho học sinh.<br>
Chức năng của Câu lạc bộ:<br>
• Câu lạc bộ bóng bàn hoạt động trong lĩnh vực rèn luyện, học tập bóng bàn, những hoạt động có mục đích khác không nằm trong lĩnh vực hoạt động của Câu lạc bộ.<br>
• Tổ chức các hoạt động học tập, rèn luyện, bồi dưỡng, vui chơi lành mạnh, tổ chức các cuộc thi.<br>
 
Điều 2. Hoạt động của Câu lạc bộ bóng bàn<br>
2.1. Nguyên tắc hoạt động của Câu lạc bộ bóng bàn<br>
• Câu lạc bộ bóng bàn hoạt động dựa trên nguyên tắc tự nguyện tham gia của các thành viên, dân chủ trong hoạt động và quản lý tập trung.<br>
• Câu lạc bộ hoạt động theo sự tổ chức và điều hành của Ban chủ nhiệm Câu lạc bộ, nhưng chịu sự quản lý, giám sát của Đoàn trường. <br>
(Nội dung sinh hoạt: Tập luyện bóng bàn cho người mới và đã biết chơi. Tổ chức các kèo thi đấu nhằm tăng sự vui nhộn trong CLB. Thỉnh thoảng tổ chức một buổi club meeting tại các quán cà phê, chơi mèo nổ, UNO, ma sói,…)<br>
- Các thành viên của Câu lạc bộ phải tự mang vợt của mình đi. Nếu không có vợt thì có thể mượn của người chơi khác trong lúc nghỉ giải lao.<br>
 
2.2. Hình thức hoạt động của Câu lạc bộ tiếng Anh<br>
2.2.1. Các hoạt động thường xuyên<br>
• Câu lạc bộ tổ chức và duy trì các hoạt động thường xuyên theo kế hoạch được xây dựng.<br>
• Ban chủ nhiệm Câu lạc bộ phối hợp với BCH Đoàn trường hoặc các cá nhân, đơn vị phối hợp, hợp tác tổ chức các hoạt động thường xuyên và các hoạt động tư vấn về phương pháp, các khoá học bóng bàn, các kèo thi đấu bóng bàn.<br>
• Câu lạc bộ tập hợp một khoản kinh phí (đóng góp của các thành viên) để trang bị các quả bóng bàn 3 sao.<br>
2.2.1. Các hoạt động thường kỳ<br>
• Câu lạc bộ xây dựng kế hoạch tổ chức và duy trì các hoạt động hằng ngày vào mỗi buổi chiều<br>
• Bên cạnh đó, câu lạc bộ có thể tổ chức các hoạt động ngoài trường như đi picnic, cắm trại và coffee. <br>
Điều 3. Tổ chức và điều hành Câu lạc bộ
• Câu lạc bộ được tổ chức và điều hành bởi Ban chủ nhiệm câu lạc bộ. Ban chủ nhiệm có các Nhóm chuyên trách: Nhóm Huấn luyện bóng bàn, Nhóm Biên tập nội dung, Nhóm Quản lý thành viên, và Nhóm Quản lý tài chính. Ban cố vấn thực hiện chức năng cố vấn và thẩm định các chương trình và nội dung hoạt động của Câu lạc bộ.<br>
3.1.Ban chủ nhiệm:<br>
Gồm 2 người:<br>
+ Chủ nhiệm: Dương Tùng Anh – Lớp 12C4 (Là người điều hành CLB, đưa ra những phương hướng và quyết định cho CLB. Mọi hoạt động của CLB đều phải được chủ nhiệm phê duyệt).<br>
+ 2 phó chủ nhiệm: Đinh Hà Vi – Lớp 10C1, Trần Đức Minh - Lớp 11A3 (Hỗ trợ cho chủ nhiệm đưa ra những phương hướng và cách thức hoạt động của clb).<br>
Điều 4: Hình thức hoạt động của CLB<br>
-Tổng số thành viên là 10 người. CLB hoạt động theo hướng điều hành của chủ nhiệm và phó chủ nhiệm<br>
4.1.Nội dung 1 buổi sinh hoạt:<br>
Những người biết chơi bóng bàn sẽ dạy nhưng người chưa biết chơi bóng bàn. Cuối mỗi buổi tập sẽ có những kèo thi đấu không ăn tiền.<br>
 
Trên đây là một số phương hướng hoạt đông và quy chế hoạt động của CLB đã đề ra và áp dụng sinh hoạt trong thời gian tới nhằm phát huy tính tự chủ của các bạn học sinh và môi trường học tập bóng bàn tốt hơn.

